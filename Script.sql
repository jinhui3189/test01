CREATE TABLE TBL_STUDENT(
	STUDENT_ID NUMBER,
	STUDENT_NAME VARCHAR2(100),
	STUDENT_AGE NUMBER,
	DEPARTMENT_ID NUMBER,
	ADDRESS_ID NUMBER,
	CONSTRAINT PK_STUDENT_ID PRIMARY KEY(STUDENT_ID),
	CONSTRAINT FK_STU_DEPT FOREIGN KEY(DEPARTMENT_ID) REFERENCES TBL_DEPARTMENT(DEPARTMENT_ID),
	CONSTRAINT FK_STU_ADD FOREIGN KEY(ADDRESS_ID) REFERENCES TBL_ADDRESS(ADDRESS_ID)
);

CREATE TABLE TBL_DEPARTMENT(
	DEPARTMENT_ID NUMBER,
	DEPARTMENT_NAME VARCHAR2(100),
	CONSTRAINT PK_DEPARTMENT_ID PRIMARY KEY(DEPARTMENT_ID),
	CONSTRAINT UK_DEPARTMENT_NAME UNIQUE(DEPARTMENT_NAME)
);

CREATE TABLE TBL_ADDRESS(
	ADDRESS_ID NUMBER,
	ADDRESS_POSTAL_CODE NUMBER,
	ADDRESS_LINE1 VARCHAR2(100),
	ADDRESS_LINE2 VARCHAR2(100),
	CONSTRAINT PK_ADDRESS_ID PRIMARY KEY(ADDRESS_ID)
);

